//swapping two variables

 class Swapping {
 
 public static void main(String[] args) {
     
   int a, b, temp;
   a = 20;
   b = 25;
   System.out.println("Before swapping : a, b = "+a+", "+ + b);
   temp = a;
   a = b;
   b = temp;   
  System.out.println("After swapping : a, b = "+a+", "+ + b);
 }}

